const languageLabels = {
  en: 'EN',
  fr: 'FR',
  ar: 'AR',
}

const languageNames = {
  en: 'English',
  fr: 'Français',
  ar: 'العربية',
}

const languageLocales = {
  en: 'en-US',
  fr: 'fr-FR',
  ar: 'ar-MA',
}

const supportedLanguages = Object.keys(languageLabels)

const I18nContext = createContext(null)

const getNestedValue = (obj, path) => {
  if (!obj) return undefined
  return path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), obj)
}

const formatMessage = (message, params) => {
  if (typeof message !== 'string') return message
  return message.replace(/\{\{(\w+)\}\}/g, (_, key) => {
    const value = params?.[key]
    return value === undefined || value === null ? '' : String(value)
  })
}

export const I18nProvider = ({ children }) => {
  const [language, setLanguageState] = useState(() => {
    const saved = localStorage.getItem('language')
    return supportedLanguages.includes(saved) ? saved : 'en'
  })

  useEffect(() => {
    const root = document.documentElement
    root.lang = language
    root.dir = language === 'ar' ? 'rtl' : 'ltr'
  }, [language])

  const setLanguage = (nextLanguage) => {
    if (!supportedLanguages.includes(nextLanguage)) return
    setLanguageState(nextLanguage)
    localStorage.setItem('language', nextLanguage)
  }

  const t = useMemo(() => {
    return (key, params = {}) => {
      const value =
        getNestedValue(translations[language], key) ?? getNestedValue(translations.en, key)
      if (!value) return key
      return formatMessage(value, params)
    }
  }, [language])

  const tOption = useMemo(() => {
    return (group, key, fallback = '') => {
      if (!key) return fallback
      const value =
        getNestedValue(translations[language], `options.${group}.${key}`) ??
        getNestedValue(translations.en, `options.${group}.${key}`)
      return value || fallback || key
    }
  }, [language])

  const value = useMemo(() => {
    return {
      language,
      setLanguage,
      t,
      tOption,
      languageLabels,
      languageNames,
      locale: languageLocales[language] || 'en-US',
      supportedLanguages,
    }
  }, [language, t, tOption])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export const useI18n = () => {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}
